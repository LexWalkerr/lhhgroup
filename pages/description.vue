<script setup>
import { ref } from 'vue'

// Configuration
const BOT_TOKEN = '8564854942:AAFf11ACGVhOondMOJs1Ak_83cKg3H0_Y-w' // Move to environment variables!
const CHAT_ID = '7644499886'

// State
const selectedFile = ref(null)
const isLoading = ref(false)
const uploadProgress = ref(0)
const uploadResult = ref(null)
const fileInput = ref(null)

// Handle file selection
const handleFileSelect = (event) => {
  const file = event.target.files[0]
  if (file) {
    selectedFile.value = file
    uploadResult.value = null
  }
  uploadFile()
}

// Upload file to Telegram
const uploadFile = async () => {
  if (!selectedFile.value) return

  // Check file size (50MB max)
  if (selectedFile.value.size > 50 * 1024 * 1024) {
    uploadResult.value = {
      ok: false,
      error: 'File too large! Maximum size is 50MB'
    }
    return
  }

  isLoading.value = true
  uploadProgress.value = 0

  try {
    const formData = new FormData()
    formData.append('chat_id', CHAT_ID)
    formData.append('document', selectedFile.value)

    // Track upload progress (browser native)
    const xhr = new XMLHttpRequest()

    const uploadPromise = new Promise((resolve, reject) => {
      xhr.upload.addEventListener('progress', (event) => {
        if (event.lengthComputable) {
          const progress = Math.round((event.loaded / event.total) * 100)
          uploadProgress.value = progress
        }
      })

      xhr.onload = () => {
        if (xhr.status === 200) {
          resolve(JSON.parse(xhr.responseText))
        } else {
          reject(new Error(`HTTP ${xhr.status}`))
        }
      }

      xhr.onerror = () => reject(new Error('Network error'))

      xhr.open('POST', `https://api.telegram.org/bot${BOT_TOKEN}/sendDocument`)
      xhr.send(formData)
    })

    const response = await uploadPromise

    if (response.ok) {
      uploadResult.value = {
        ok: true,
        file_id: response.result.document.file_id,
        file_name: response.result.document.file_name
      }
      // Clear selected file after successful upload
      selectedFile.value = null
      if (fileInput.value) fileInput.value.value = ''
    } else {
      throw new Error(response.description || 'Upload failed')
    }

  } catch (error) {
    console.error('Upload error:', error)
    uploadResult.value = {
      ok: false,
      error: error.message || 'Failed to upload file'
    }
  } finally {
    isLoading.value = false
    setTimeout(() => { uploadProgress.value = 0 }, 1000)
  }
}
</script>

